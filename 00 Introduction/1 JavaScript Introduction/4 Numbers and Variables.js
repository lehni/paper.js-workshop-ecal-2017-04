// Just like with strings, we can also put numbers into variables

var myNumber = 10;
var myBiggerNumber = 100;

console.log(myNumber * myBiggerNumber);

// We can also combine variables and numbers:
console.log(myNumber * myBiggerNumber + 100);

// What's great with variables is that if we would now change the definition of
// myNumber on line 3, both of these calculations would automatically produce
// other results, although we only changed one value.
// Variables are a convenient way to reference a value by name, instead of using
// the value directly.